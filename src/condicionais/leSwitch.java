package condicionais;

public class leSwitch {
    public static void main(String[] args) {
        int num = 6;
        switch (num) {
            case 3:
                System.out.println("O número é 3");
                break;
            case 5:
                System.out.println("O número é 5");
                break;
            default:
                System.out.println("O número é " + num);
                break;
    }
}
}
