public class tiposDados {
    public static void main(String[] args) {
        //tipos primários/primitivos
        int num0 = 4; //inteiro
        long num1 = 99999999999999l; // Inteiro muito grande
        short num2 = 3; //inteiro (número curto)
        byte num3 = 4; //inteiro mas utiliza menos memória
        float num4 = 23.4f; //por padrão os números com ponto flutuante são considerados double
        double num5 = 23.4;
        char num6 = 2; // um caractere

        //alfanuméricos:
        char letra = 'a'; // um caractere, SEMPRE COM ASPAS SIMPLES
        //NÃO EXISTE O TIPO PRIMITIVO string

        //tipos booleanos:
            boolean verdadeiro = true;
            boolean falso = false;

        //tipos não primarios/primitivos
        Integer numA = 98; //uma classe que representa o int
        Long numB = 999999999999L;
        Short numC = 7; //uma classe que representa o short
        Byte numD = 4; //uma classe que representa o byte
        Float numE = 23.4f; //uma classe que representa o float
        Double numF = 23.4; //uma classe que representa o double
        Character name0 = 'a';
        String name1 = "nome"; //SEMPRE SERÁ NÃO PRIMITIVO
        Boolean vdd = true;
        Boolean fls = false;

    };
}
