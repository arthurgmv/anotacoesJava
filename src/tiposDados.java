public class tiposDados {
    public static void main(String[] args) {
        //tipos primários/primitivos
        int num1 = 4; //inteiro
        short num2 = 3; //inteiro (número curto)
        byte num5 = 4; //inteiro mas utiliza menos memória
        float num3 = 23.4f; //por padrão os números com ponto flutuante são considerados double
        double num4 = 23.4;



        //tipos não primarios/primitivos
        Integer numA = 98; //uma classe que representa o int
        Short numB = 7; //uma classe que representa o short
        Byte numC = 4; //uma classe que representa o byte
        Float numD = 23.4f; //uma classe que representa o float
        Double numE = 23.4; //uma classe que representa o double

    }
}
